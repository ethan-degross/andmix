<div class="gallery-item" >
<iframe class="iframe" src="https://www.youtube.com/embed/8qQx8bicF2w" allowfullscreen></iframe>
<h4 class="writtencontent">"By the Riverside, He Shines"</h4>
<p class="artist">Artist: Emily Swanson</p>
<p class="writtencontent">License: CC BY </p>
<div class="member">
    <button class="button">Artist Statement</button>
</div>
<div class="answer">
    <p class="hide-show">My song mashup answers the question "who/what owns creativity?"
<br><br>
Behind most all forms of creativity are passion and the motivation to express that passion. Even if someone, such as the boogie woogie bugle boy described in my remix, is in a circumstance where they feel trapped or cannot publicly express their passion, they still have the ability be creative.
<br><br>
Psychologist, Mihaly Csiksgentmihalyi, defines creativity by three components: the domain, the field, and the person. I, however, disagree that the field defines creativity. If a tree falls in a forest and no one is around to hear it, does it make a sound? Just because we don't see something happen, doesn't mean that it did not occur. Creativity doesn't need to be told it is creative.
<br><br>
Though the domain, field, and person are all necessary for allowing the general public to recognize something as creative, they aren't all necessary to achieve creativity. I believe that the answer to the question, "what is creative?" differs from person to person, so something that appears creative to you may seem overdone and unoriginal to someone else.
<br><br>
The man in my remix was famous in Chicago for his ability to write and play music on the trumpet, but then he was drafted. The only time he was able to play his trumpet the way he wanted to was at night, when he sneaked out to play it by the riverside. Was he no longer creative because he didn't have an audience? He had a passion for jazz music and the motivation to sneak out in order to play it. He was creative.
<br><br>
In a podcast with On Being Studios, Rex Jung defines creativity as "something both novel and useful" and I prefer this definition over Csikszentmihalyi's definition. I like this definition because it acknowledges individuality. Something I consider novel and useful may not be novel and useful to someone else. The man in my remix found playing the trumpet to be both novel and useful, which means he was being creative. His music was novel because he played the trumpet in a way that differed from the norm and useful because it gave him hope while at war that he could make it home one day.
<br><br>
I choose to make my image the trumpet flower because trumpet flowers are able to grow by rivers.
</p>
